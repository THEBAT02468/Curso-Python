import math

print(round(1.3))  # aproximado
print(round(1.7))
print(round(1.5))
print(abs(55))  # valor absoluto
print(abs(-77))  # valor absoluto

print(math.ceil(1.1))  # numero cercano mas cercano pero positivo
print(math.floor(1.99999))  # numero entero mas cercano pero negativo
print(math.isnan(23))  # numeros reales
print(math.pow(10, 3))  # potencia
print(math.sqrt(9))  # raiz cuadrada
